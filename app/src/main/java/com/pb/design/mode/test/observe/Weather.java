public class Weather {
    private int temp;
    private int hum;
    private int wind;

    public int getTemp() {
        return temp;
    }

    public void setTemp(int temp) {
        this.temp = temp;

    }

    public int getHum() {
        return hum;
    }

    public void setHum(int hum) {
        this.hum = hum;

    }

    public int getWind() {
        return wind;
    }

    public void setWind(int wind) {
        this.wind = wind;

    }
}
